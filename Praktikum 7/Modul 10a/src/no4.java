public class no4 {
    public static void main (String[]args){
        int z = 78;
        char output = (char) z;

        System.out.println(output);
    }
}

//Penjelasan
// Output yang dihasilkan adalah N karena mengonversi tipe data numerik seperti int menjadi tipe data char,
// yang terjadi adalah konversi nilai ASCII dari angka integer tersebut.
// Jadi, nilai z, yaitu 78, akan dikonversi menjadi karakter yang sesuai dengan nilai ASCII 78.
